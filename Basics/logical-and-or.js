let temp = 150;

// logical and operator - True if both sides are true. False otherwise
if (temp >= 60 && temp <= 90) {
    console.log('It is pretty nice out!')
};

// Logical or operator - true if at least on side is true. False otherwise
if (temp <= 0 || temp >= 110) {
    console.log('do not go outside')
};
