let fahrenheit = 32

// Calculate celsius and store in celsius variable
let celsius = (fahrenheit - 32) * 1.8;
// or
let celsius2 = (fahrenheit - 32) * 5 / 9;
// print that value
console.log(celsius);
console.log(celsius2);

// calculate kelvin value and store in variable
let kelvin = (fahrenheit + 459.67) * 0.55555556;
// or
let kelvin2 = (fahrenheit + 459.67) * 5 / 9;
// print that value
console.log(kelvin);
console.log(kelvin2);